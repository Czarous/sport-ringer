			        <h2>APF stun Machhindra to reach final</h2>
			        <p>Departmental team APF FC stunned 10-man Machhindra Football Club 3-1 to enter into the Aaha Rara Gold Cup Football Tournament final at the Pokhara Stadium on Wednesday.<br>

                        Defender Sanish Shrestha, Pradeep Lama and captain Nabin Lama were on target for APF, the relegation strugglers at the ongoing Martyrs Memorial 'A' Division League, to inflict league leaders Machhindra their first defeat of the season.
                        
                        Nigerian defender Omolaja Kareem brought Machhindra back in contention with a first-half equaliser before being sent off after the restart.<br>
                        
                        APF, who secured a final berth for the second time in the longest running annual football tournament of the country, will vie between the winners of the second semi-final scheduled between Tribhuvan Army FC and Manang Marshyangdi Club in the summit clash on Saturday. The second semi-final will be played on Thursday.
                        
                        APF's best run in the tournament dates back to 2008 when they were defeated 2-0 by Nepal Police Club in the final.
                    </p>

			        <p> The entertaining first semi-final saw APF take a 25th minute lead through defender Shrestha, who made a towering header inside the crowded zone to turn in a Mani Lama freekick. Machhindra, playing in the absence of seven regular players inducted in the national team camp for Mauritius friendlies, replied through Kareem who headed to convert skipper Ranjeet Dhimal's freekick.

                        <br>APF restored the one-goal cushion in the 68th through Pradeep Lama who made a perfect run to tap in Aashish's right flank cross to beat the advancing goalie Surya Adhikari inside the area. Three minutes later, Machhindra were limited to 10-man after Kareem was shown red for expressing dissent to the referee Kabin Byanjankar's decision.
                        
                        Captain Nabin sealed the victory for the APF in the last minute of the five-minute injury time. He applied a finishing touch at the open goalpost after the forward found a ball blocked by goalie Adhikari in his attempt to deny Awash Lamichhane's strike on goal.
                        
                        <br>Machhindra coach Prabesh Kautwal said that absence of seven players due to national camp and injuries of two members in the last match made life difficult for them. "Rejin Subba and Utsav Rai are yet to recover fully after the injury they picked up in the last match. Though it was an evenly contested game, we lost after we got limited to 10 players," said Kautwal.
                        
                        APF coach Kumar Thapa expressed satisfaction at his team's hard effort. "The players are energised to play in Pokhara after the league break," he said.
